#include <iostream>
using namespace std;
int main()
{
    int a;
    cout << "Size of Int a is " << sizeof(a) << " Bytes" << endl;
    char b;
    cout << "Size of char b  is " << sizeof(b) << " Bytes" << endl;
    bool c;
    cout << "Size of bool  c  is " << sizeof(c) << " Bytes" << endl;
    float d;
    cout << "Size of float d  is " << sizeof(d) << " Bytes" << endl;
    int short e;
    cout << "Size of int short e  is " << sizeof(e) << " Bytes" << endl;
    int long f;
    cout << "Size of int long f  is " << sizeof(f) << " Bytes" << endl;
}